import sortByHealth from '../src/js/sortedhealth';

test('should compare smthg with smthg', () => {
  const input = [
    { name: 'мечник', health: 10 },
    { name: 'маг', health: 100 },
    { name: 'лучник', health: 80 },
  ];

  const expected = [
    { name: 'маг', health: 100 },
    { name: 'лучник', health: 80 },
    { name: 'мечник', health: 10 },
  ];

  const received = sortByHealth(input);

  expect(received).toEqual(expected);
});
